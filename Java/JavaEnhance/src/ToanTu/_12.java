package ToanTu;

public class _12 {

	public static void main(String[] args) {
		int i = 10;
		int n = i++ % 5;
		System.out.println(i);
		System.out.println(n);
		int i2 = 10;
		int n2 = ++i2 % 5;
		System.out.println(i2);
		System.out.println(n2);
		int i3 = 8;
		i3 >>= 2;
		System.out.println(i3);
		int i4 = 17;
		i4 >>= 1;
		System.out.println(i4);
	}

}
